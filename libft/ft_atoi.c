#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		polarity;
	int		result;

	i = 0;
	polarity = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			polarity = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	result = result * polarity;
	return (result);
}

